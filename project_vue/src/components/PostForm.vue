<template>
  <form @submit.prevent>
    <h4>Создать пост</h4>
    <my-input
        v-model="post.title"
        type="text"
        placeholder="Название поста"
    />
    <my-input
        v-model="post.body"
        type="text"
        placeholder="Наполнение поста"
    />
    <my-button class="btn"
            @click="createPost"
               style="align-self: flex-end; margin-top: 15px"
    >Добавить пост
    </my-button>
  </form>
</template>

<script>


export default {
data(){
  return {
    post: {
      title: '',
      body: ''
    }
  }
},
  methods: {
    createPost(){
      this.post.id = Date.now();
      this.$emit('create', this.post)
      this.post = {
        title: '',
        body: ''
      }
    }
  }
}
</script>

